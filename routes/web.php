                                        desenvolveu, tornando-se um jogo que faz parte da cultura popular brasileira. Sua origem
                                        remonta a Itália do século XVI. De acordo com estudos, o embrião do jogo surgiu na
                                        Itália, no início do século XVI, pelos indivíduos que realizavam as eleições dos membros
                                        da Câmara e do Senado de algumas cidades através de sorteios.</p>
                                    <p class="addlinks">Os candidatos eram nomeados em bolinhas sorteadas em uma urna. A partir
                                        daí improvisou-se em jogo, disseminado depois para outros países da Europa. Tem se
                                        registro da prática na França, pelo exército de Carlos VIII. Após 1539, o jogo passou a
                                        oferecer prêmios e a renda revertida ao Tesouro.</p>
                                    <p class="addlinks"> A palavra 'bingo' é de origem inglesa, onde por sinal o jogo foi
                                        introduzido por volta de 1870, tornou-se tradição na terra da Rainha, sendo
                                        regulamentado e muito praticado. Na Alemanha do século XIX, o bingo chegou a ser usado
                                        nas escolas como auxílio de ensino de matemática. No entanto, foram os norte-americanos
                                        os maiores responsáveis pelo game ganhar as fronteiras de todo o mundo e também chegar
                                        ao Brasil. Introduzido nos Estados Unidos em 1929, os bingos se expandiram rapidamente
                                        quando começaram a angariar recursos e a distribuir prêmios. Foram os norte-americanos
                                        que envolveram dinheiro no <em>jogo de bingo</em>, medida que popularizou o jogo de
                                        forma muita rápida.</p>
