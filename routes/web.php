                                        uma passagem por Jacksonville, na Geórgia, Lowe encontrou um grupo de pessoas em uma
                                        festa popular bastante animadas na disputa de um jogo chamado Beano, em que as pessoas
                                        marcavam números em cartelas marcadas por feijão. Cada vez que um nativo completava uma
                                        cartela, ele gritava Beano, causando empolgação entre os presentes. Fascinado pelo grau
                                        de entretenimentos dos locais com o jogo, Lowe teve a idéia de adaptar a mecânica e as
                                        regras de bingo para a indústria comercial, adotando o nome de bingo. O jogo tornou-se
                                        um sucesso em poucos dias, espalhando-se pelos Estados Unidos, antes de ganhar o mundo.
                                        Foram também os norte-americanos que introduziram as modalidades financeiras, ou seja, o
                                        <em>jogo de bingo</em> envolvendo dinheiro com possibilidade de ganhos e perdas reais. O
                                        Brasil, os Estados Unidos e o México são três dos países recordes em número de pessoas
                                        que jogam bingo. Porém, em termos de legislação, a Espanha está na frente, tendo
                                        regulamentado os bingos em 1978. Exemplo parecido seguiram vizinhos nossos
